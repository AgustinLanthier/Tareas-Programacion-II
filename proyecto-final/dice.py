#!/usr/bin/python

import sys
import random

# Códigos ANSI de colores
BLACK = "\033[1;30m"
RED = "\033[1;31m"
GREEN = "\033[1;32m"
YELLOW = "\033[1;33m"
BLUE = "\033[1;34m"
MAGENTA = "\033[1;35m"
CYAN = "\033[1;36m"
WHITE = "\033[1;37m"
RESET = "\033[0m"

def roll4():
    Rn = random.randint(1, 4)
    print(f"""{YELLOW}
          ;;
        ,;  ;,
       ,;    ;,
      ,;      ;,
     ,;        ;,
    ,;          ;, 
   ,;            ;,
  ,;              ;,
 ,;                ;, 
,;        {BLUE}{Rn}{YELLOW}         ;,
::::::::::::::::::::::
{RESET}""")

def roll6():
    Rn = random.randint(1, 6)
    print(f"""{GREEN}
 ::::::::::::::
 ::          ::  
 ::          ::
 ::    {RED}{Rn}{GREEN}     ::
 ::          ::
 ::          ::                
 :::::::::::::: 
{RESET}""")

def double6():
    Rn = random.randint(1, 6)
    Rm = random.randint(1, 6)
    print(f"""{BLUE}
 ::::::::::::::   ::::::::::::::
 ::          ::   ::          ::
 ::          ::   ::          ::
 ::    {YELLOW}{Rn}{BLUE}     ::   ::    {YELLOW}{Rm}{BLUE}    ::
 ::          ::   ::          ::
 ::          ::   ::          ::             
 ::::::::::::::   ::::::::::::::
{RESET}""")

def roll20(num):
    if num < 10:
        number_str = f"  {RED}{num}{WHITE}   "
    else:
        number_str = f" {GREEN}{num}{WHITE}   "

    print(f"""{WHITE}
            ,:::,
       ,,,:;  :  ;:,,, 
   ,,,:       :       :,,, 
,,;...........:...........;,,
; ;          ;';          ; ;
;  ;        ;   ;        ;  ;
;   ;      ;     ;      ;   ;
;    ;    ; {number_str};    ;    ;
;     ;  ;         ;  ;     ;
;      ;:...........:;      ;
;     , ;           ; ,     ;
;   ,'   ;         ;   ',   ;
'';'      ;       ;      ';''  
   ''';    ;     ;    ;'''         
       ''':;;   ;;:'''
            ':::' 
{RESET}""")

    if num == 20:
        print(f"{CYAN}¡NAT 20! 🎉{RESET}")

def mostrar_ayuda():
    print(f"""{WHITE}
Uso:
  python dados.py [opción]

Opciones:
  -4     Tirar dado de 4 caras (d4)      ({YELLOW}amarillo{WHITE})
  -6     Tirar dado de 6 caras (d6)      ({GREEN}verde{WHITE})
  -d6    Tirar dos dados de 6 caras      ({BLUE}azul{WHITE})
  -20    Tirar dado de 20 caras (d20)    ({MAGENTA}magenta{WHITE})
{RESET}""")

if __name__ == "__main__":
    if len(sys.argv) < 2:
        mostrar_ayuda()
        sys.exit(1)

    arg = sys.argv[1]

    if arg == "-4":
        roll4()
    elif arg == "-6":
        roll6()
    elif arg == "-d6":
        double6()
    elif arg == "-20":
        roll20()
    else:
        print(f"{RED}Opción no válida.{RESET}")
        mostrar_ayuda()
